package ojc.user.model;

public class User {
	private String empId;
	private String password;
	private String createId;
	private String updateId;
	private String deleteYn;
	private String positionCode;
	private String createDate;
	private String updateDate;
	
	public final String getEmpId() {
		return empId;
	}
	public final void setEmpId(String empId) {
		this.empId = empId;
	}
	public final String getPassword() {
		return password;
	}
	public final void setPassword(String password) {
		this.password = password;
	}
	public final String getCreateId() {
		return createId;
	}
	public final void setCreateId(String createId) {
		this.createId = createId;
	}
	public final String getUpdateId() {
		return updateId;
	}
	public final void setUpdateId(String updateId) {
		this.updateId = updateId;
	}
	public final String getDeleteYn() {
		return deleteYn;
	}
	public final void setDeleteYn(String deleteYn) {
		this.deleteYn = deleteYn;
	}
	public final String getPositionCode() {
		return positionCode;
	}
	public final void setPositionCode(String positionCode) {
		this.positionCode = positionCode;
	}
	public final String getCreateDate() {
		return createDate;
	}
	public final void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public final String getUpdateDate() {
		return updateDate;
	}
	public final void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	
	
	
	
}
